public class RunData {
    String user_id;
    String run_type;
    String run_date;
    String run_time;
    String run_km;
    String run_place;
    String run_person_num;

    public String getRun_km() {
        return run_km;
    }

    public void setRun_km(String run_km) {
        this.run_km = run_km;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getRun_type() {
        return run_type;
    }

    public void setRun_type(String run_type) {
        this.run_type = run_type;
    }

    public String getRun_date() {
        return run_date;
    }

    public void setRun_date(String run_date) {
        this.run_date = run_date;
    }

    public String getRun_time() {
        return run_time;
    }

    public void setRun_time(String run_time) {
        this.run_time = run_time;
    }

    public String getRun_place() {
        return run_place;
    }

    public void setRun_place(String run_place) {
        this.run_place = run_place;
    }

    public String getRun_person_num() {
        return run_person_num;
    }

    public void setRun_person_num(String run_person_num) {
        this.run_person_num = run_person_num;
    }
}
